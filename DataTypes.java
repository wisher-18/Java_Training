
public class DataTypes {

	public static void main(String[] args) {
		byte a= 100;
		short b = 150;
		int c = 200;
		long l = 300;
		
		char ch = 'a';
		String name = "Bhushan";
		
		float z = 10.0f;
		double d = 100.200;
		
		boolean flag = true;
		
		System.out.println("Byte: "+a+"\nShort: "+b+"\nInt: "+c+"\nLong: "+l+"\n\nChar: "+ch+"\nString: "+name+"\nFloat: "+z+
				"\nDouble: "+d+"\n\nBoolean: "+flag);
		
		

	}

}
